// データ取得関数
export function getSimulationData(interestRate, monthlyAmount, years) {
  const key = `${interestRate}_${monthlyAmount}_${years}`;
  const selectedData = simulationData[key];

  if (!selectedData) {
    return null;
  }

  // 2%利率のデータからbaseTotalを取得
  const baseKey = `2_${monthlyAmount}_${years}`;
  const baseData = simulationData[baseKey];

  if (!baseData) {
    return null;
  }

  // principalDataからprincipalを取得
  const principalKey = `${monthlyAmount}_${years}`;
  const principal = principalData[principalKey];

  if (principal === undefined) {
    return null;
  }

  return {
    principal,
    baseTotal: baseData.baseTotal,
    selectedTotal: selectedData.selectedTotal,
    difference: selectedData.difference,
  };
}

/**
 * 各年のデータを取得
 * @param {number} interestRate - 利率（%）
 * @param {number} monthlyAmount - 月額積立額
 * @param {number} years - 期間（年）
 * @returns {Array<{year: number, principal: number, baseTotal: number, selectedTotal: number}>}
 */
export function getYearlyData(interestRate, monthlyAmount, years) {
  const data = [];

  // 0年目のデータ
  data.push({
    year: 0,
    principal: 0,
    baseTotal: 0,
    selectedTotal: 0,
  });

  // 1年目から最終年までのデータを取得
  for (let year = 1; year <= years; year++) {
    // 2%利率のデータからbaseTotalを取得
    const baseKey = `2_${monthlyAmount}_${year}`;
    const baseData = simulationData[baseKey];

    if (!baseData) {
      // 2%利率のデータがない場合はスキップ
      continue;
    }

    // principalDataからprincipalを取得
    const principalKey = `${monthlyAmount}_${year}`;
    const principal = principalData[principalKey];

    if (principal === undefined) {
      continue;
    }

    // 選択利率のデータからselectedTotalを取得
    const selectedKey = `${interestRate}_${monthlyAmount}_${year}`;
    const selectedData = simulationData[selectedKey];

    if (interestRate === 2) {
      // 2%利率の場合はbaseTotalをselectedTotalとして使用
      data.push({
        year,
        principal,
        baseTotal: baseData.baseTotal,
        selectedTotal: baseData.baseTotal,
      });
    } else if (selectedData) {
      // 選択利率のデータがある場合
      data.push({
        year,
        principal,
        baseTotal: baseData.baseTotal,
        selectedTotal: selectedData.selectedTotal,
      });
    } else {
      // 選択利率のデータがない場合は補間
      const prevYear = year - 1;
      const prevSelectedKey = `${interestRate}_${monthlyAmount}_${prevYear}`;
      const prevSelectedData = simulationData[prevSelectedKey];

      // 最終年のデータを取得
      const finalSelectedKey = `${interestRate}_${monthlyAmount}_${years}`;
      const finalSelectedData = simulationData[finalSelectedKey];

      if (prevSelectedData && finalSelectedData) {
        // 線形補間
        const progress = year / years;
        const prevSelectedTotal = prevSelectedData.selectedTotal;
        const finalSelectedTotal = finalSelectedData.selectedTotal;
        data.push({
          year,
          principal,
          baseTotal: baseData.baseTotal,
          selectedTotal: Math.round(prevSelectedTotal + (finalSelectedTotal - prevSelectedTotal) * progress),
        });
      } else {
        // 補間できない場合は前年のデータを使用
        const lastData = data[data.length - 1];
        data.push({
          year,
          principal,
          baseTotal: baseData.baseTotal,
          selectedTotal: lastData ? lastData.selectedTotal : baseData.baseTotal,
        });
      }
    }
  }

  return data;
}

// 数値を3桁区切りでフォーマット
export function formatNumber(num) {
  return num.toLocaleString('ja-JP');
}

// カウントアップアニメーション関数
export const startCountUpAnimation = (targetElem, to, duration = 1000) => {
  if (!targetElem) {
    return;
  }

  const from = 0;
  const startTime = performance.now();

  const countUp = () => {
    const elapsed = performance.now() - startTime;
    const countValue = from + (elapsed / duration) * (to - from);
    if (elapsed >= duration) {
      targetElem.textContent = formatNumber(to);
    } else {
      targetElem.textContent = formatNumber(Math.floor(countValue));
      requestAnimationFrame(countUp);
    }
  };

  requestAnimationFrame(countUp);
};
